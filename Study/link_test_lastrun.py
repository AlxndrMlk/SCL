def build_query(params, url):
    
    parametrized_url = url
    
    len_ = len(params)
    
    for i, param in enumerate(params):
        addon = ''
        if i + 1 < len_:
            addon = '+'
        parametrized_url += param + addon
    
    return parametrized_url
    
